						<h2>MTS (1901-1910) </h2> 
						<p>The Technological University of the Philippines was first established as the Manila Trade School in 1901 upon the enactment of Act No. 74 by the United States Philippine Commission for the instruction of the Filipinos on useful trades. Originally housed near Ateneo de Manila in Intramuros, MTS transferred to the Exposicion Regional de Filipinos in Calle Padre Faura. Six years later, the MTS transferred to the City Shops in Calle Arroceros. Industrial courses which included carpentry, English, telegraphy, machine shop work, furniture making, wood carving, boat building, plumbing, blacksmithing, mechanical and architectural drawing were offered on the primary school level during this period because pupils at that time were already adults. They were studying certain trades which they intended to follow after leaving school.</p>
						<h2>AT THE HELM THROUGH A HUNDRED YEARS OF LEADERSHIP AND EXCELLENCE IN TECHNOLOGY EDUCATION</h2>
						<p>Ronald P. Gleason was the first principal (1901-1904) of the trade school; J.J. Eaton was the first superintendent (1905-1906); and Gregorio J. Sevilla was the first Filipino Superintendent (1936-1939). Prof. Jose S. Roldan became the first president of PCAT (1959-1961) and Prof. Apolinario G. Apilado succeeded him as the second president (1962-1967). Dr. Jose R. Vergara was the third president of PCAT and the first president of TUP (1968-1988). He was succeeded by Dr. Frederick So. Pada, the second TUP president (1988-2000). Dr. Bernardo F. Adiviso served as third president (Aug. 1-16, 2000) followed by Dr. Fedeserio C. Camarao who became the fourth and centennial president (March 28,2001-March 27, 2005). Dr. Godofredo E. Gallega served as the fifth president (October 10, 2005-October 9, 2009); Dr. Olympio V. Caparas is the sixth president (October 16, 2009 to April 2014); and Dr. Adora S. Pili is the seventh and first woman president of TUP.

							With more than a hundred years of existence since its establishment as MTS, TUP has indeed transformed its "trade school" image into a dynamic premier state university in the fields of industrial, technological and engineering education. Its mark of excellence is not only known locally but also internationally.

